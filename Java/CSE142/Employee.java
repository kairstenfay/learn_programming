// Class to represent a general employee at a law firm.
public class Employee {
   // Returns the number of hours per week the employee works.
   public int getHours() {
      return 40;
   }
   
   // Returns the employees salary.
   public double getSalary() {
      return 40000.0;
   }
   
   // Returns the number of vacation days the employee gets per year.
   public int getVacationDays() {
      return 10;
   }
   
   // Returns the color of the form the employee uses to request vacation.
   public String getVacationForm() {
      return "yellow";
   }
}